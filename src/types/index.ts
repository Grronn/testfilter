// Периоды для первого дропдауна
export type Period = "все" | "последняя" | "сегодня" | "неделя" | "месяц";

// Тип сущности для поиска (переключатель участок/общество)
// участок = геологический участок/площадка
// общество = юридическое лицо (ООО, подрядчик, заказчик)
export type EntityType = "участок" | "общество";

// Чип — выбранный элемент фильтра (отображается под панелью)
export interface FilterChip {
  id: string;
  type: "date" | "entity";
  label: string; // отображаемый текст
  value: string; // значение для фильтрации
}

// Состояние фильтров формы (то что хранит React Hook Form)
export interface SvodkiFilters {
  period: Period;
  entityType: EntityType;
  search: string;    // текущий ввод в поле поиска
  selectedDate: string; // выбранная дата из датапикера (YYYY-MM-DD)
  chips: FilterChip[]; // все выбранные чипы (и даты и сущности)
}

export const DEFAULT_FILTERS: SvodkiFilters = {
  period: "все",
  entityType: "общество",
  search: "",
  selectedDate: "",
  chips: [],
};

// ============================================================
// ДАННЫЕ СВОДКИ
// Поля основаны на том что видно в карточках на скриншоте
// ============================================================
export interface Svodka {
  id: string;
  title: string;
  dateFrom: string;        // "сводка от" - дата в формате DD.MM.YYYY
  customer: string;        // заказчик
  contractor: string;      // подрядчик
  sites: string[];         // участки (Кудринский, Тепловский и т.д.)
  areas: string[];         // площади/объекты
}

